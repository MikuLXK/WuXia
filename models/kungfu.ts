
// 功法系统定义 (扁平化设计)

export type 功法类型 = '内功' | '外功' | '轻功' | '绝技' | '被动';
export type 功法品质 = '凡品' | '良品' | '上品' | '极品' | '绝世' | '传说';
export type 伤害类型 = '物理' | '内功' | '真实' | '混合';
export type 消耗类型 = '内力' | '精力' | '气血' | '无';
export type 目标类型 = '单体' | '全体' | '扇形' | '自身' | '随机';

// 子结构：尽量保持简单
export interface 技能效果 {
    代码: string;      // e.g. "BUFF_POISON"
    名称: string;      // e.g. "剧毒"
    触发概率: number;  // 0-100
    持续时间: number;  // 秒
    数值参数: number;  // 强度/层数
    生效间隔: number;  // 秒 (0代表一次性)
}

export interface 被动修正 {
    属性名: string;    // e.g. "力量"
    数值: number;
    类型: '固定值' | '百分比';
}

export interface 境界特效 {
    解锁重数: number;
    描述: string;
}

export interface 功法结构 {
    // --- 基础信息 (根目录) ---
    ID: string;
    名称: string;
    描述: string;
    类型: 功法类型;
    品质: 功法品质;
    来源: string;      // 门派或出处

    // --- 成长数据 (动态) ---
    当前重数: number;
    最高重数: number;
    当前熟练度: number;
    升级经验: number;  // 升级所需熟练度
    突破条件: string;  // 文本描述，如 "悟性>30"

    // --- 限制条件 (扁平化) ---
    境界限制: string;
    武器限制: string[]; // 空数组代表无限制/空手

    // --- 消耗与时间 (扁平化 - 核心修改) ---
    消耗类型: 消耗类型;
    消耗数值: number;
    施展耗时: number;  // 前摇/读条 (秒)
    冷却时间: number;  // CD (秒)

    // --- 战斗数值 (扁平化) ---
    基础伤害: number;
    加成属性: string;  // 关联属性，如 "敏捷"
    加成系数: number;  // 关联属性的倍率
    伤害类型: 伤害类型;
    
    // --- 范围 (扁平化) ---
    目标类型: 目标类型;
    最大目标数: number;

    // --- 列表数据 (仅保留必要的数组) ---
    附带效果: 技能效果[];
    被动修正: 被动修正[]; // 装备/运功时生效
    境界特效: 境界特效[];
}
