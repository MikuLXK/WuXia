
// 物品系统核心定义 (全中文变量)

// 物品尺寸等级 (数值化 1-10)
// 1: 极小 (丹药, 针, 铜钱)
// 2: 小型 (匕首, 苹果, 秘籍)
// 3: 中型 (单手剑, 衣服, 鞋子, 水囊)
// 4: 大型 (长枪, 双手斧, 筝)
// 5: 超大 (尸体, 鼎)
// ...
export type 物品尺寸 = number; 

export type 物品类型 = '武器' | '防具' | '容器' | '消耗品' | '材料' | '秘籍' | '杂物';
export type 物品品质 = '凡品' | '良品' | '上品' | '极品' | '绝世' | '传说';

// 身体部位定义
export type 身体部位 = '头部' | '胸部' | '腹部' | '左臂' | '右臂' | '左腿' | '右腿' | '手掌' | '足部';

// 随机词条/属性修饰
export interface 物品词条 {
    名称: string;      // e.g. "锋利"
    属性: string;      // e.g. "物理攻击"
    数值: number;     // e.g. 5
    类型: '固定值' | '百分比'; 
}

// 容器详细属性 (用于背包、带口袋的衣服)
export interface 容器属性 {
    最大容量: number;       // 也是基于尺寸的单位 (e.g. 背包容量20)
    当前已用空间: number;   // 实时计算
    最大单物大小: number;   // 限制口径 (e.g. 衣服口袋只能放尺寸<=2的物品)
    减重比例: number;       // 0-100 (如：玄铁箱减重0%，乾坤袋减重80%)
}

// 基础物品接口
export interface 基础物品 {
    ID: string;
    名称: string;
    描述: string;
    类型: 物品类型;
    品质: 物品品质;
    
    重量: number;        // 单位：斤/两 (数值)
    占用空间: 物品尺寸;   // 单位：格 (1-10)
    
    价值: number;        // 铜钱
    
    当前耐久: number;
    最大耐久: number;    // 0为不可破坏

    词条列表: 物品词条[]; // 随机属性
    当前容器ID?: string;  // 所在容器的ID；未收纳时可为空

    // 任何物品都可能有容器属性 (如：带暗格的剑鞘、带口袋的衣服)
    容器属性?: 容器属性;
}

// 武器扩展
export interface 武器 extends 基础物品 {
    类型: '武器';
    武器子类: '剑' | '刀' | '枪' | '棍' | '暗器' | '拳套';
    最小攻击: number;
    最大攻击: number;
    攻速修正: number; // 1.0 为标准
    格挡率: number;   // 0-100
}

// 防具扩展
export interface 防具 extends 基础物品 {
    类型: '防具';
    装备位置: '头部' | '胸部' | '腿部' | '手部' | '足部';
    覆盖部位: 身体部位[]; // 实际保护的部位
    
    物理防御: number;
    内功防御: number;
}

// 容器扩展 (纯容器物品，如背篓)
export interface 容器物品 extends 基础物品 {
    类型: '容器';
    装备位置: '背部' | '腰部'; 
    容器属性: 容器属性; // 容器物品必须有此属性
}

// 秘籍扩展
export interface 秘籍 extends 基础物品 {
    类型: '秘籍';
    对应技能名: string;
    学习要求: string; // e.g. "悟性>50"
    研读进度: number;    // 0-100
    是否消耗: boolean; // 是否阅后即焚
}

// 消耗品扩展
export interface 消耗品 extends 基础物品 {
    类型: '消耗品';
    使用效果: {
        目标属性: string; // e.g. "当前气血", "当前内力"
        数值: number;
    }[];
    毒性: number; // 丹毒
}

// 联合类型
export type 游戏物品 = 武器 | 防具 | 容器物品 | 秘籍 | 消耗品 | 基础物品;
