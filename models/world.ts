
// --- 基础定义 ---

// 严格时间格式: YYYY:MM:DD:HH:MM (例如 1024:03:01:14:00)
export type 游戏时间格式 = string; 

// --- 活跃NPC (后台模拟) ---

export interface 活跃NPC结构 {
    ID: string;
    姓名: string;
    称号: string;
    所属势力: string; // 势力名称 (e.g. "归元宗")
    境界: string;     // AI参考用
    
    // 动态时空信息
    当前位置: string;
    状态: string; // 简短描述，e.g. "闭关", "追杀", "奇遇"
    
    // 核心行动逻辑
    当前行动描述: string; // e.g. "正在前往漠北寻找楼兰古城遗址"
    行动开始时间: 游戏时间格式;
    行动预计结束时间: 游戏时间格式; // 触发判定点
    
    持有重宝: string[]; // 物品名称列表，用于吸引玩家
}

// --- 世界事件 (生命周期) ---

export type 事件状态 = '进行中' | '已结算';

export interface 世界事件结构 {
    ID: string;
    类型: '天灾' | '战争' | '奇遇' | '传闻' | '决斗' | '系统';
    标题: string;
    内容: string; // 发生时的描述
    发生地点: string;
    
    // 时间轴 (YYYY:MM:DD:HH:MM)
    开始时间: 游戏时间格式;
    预计结束时间: 游戏时间格式; // 触发结算
    
    // 结算结果
    当前状态: 事件状态;
    事件结果?: string; // 结算后由AI填充，如 "某某战胜了某某"
    
    // 生命周期
    消逝时间?: 游戏时间格式; // 结果展示一段时间后，从UI移除
    是否重大事件: boolean; // 若为true，归档至史册；否则直接删除
    
    // 索引 (使用名称)
    关联势力: string[]; // e.g. ["归元宗", "血煞教"]
    关联人物: string[]; // e.g. ["玄机子", "林清月"]
}

// --- 世界全貌 ---

export interface 世界数据结构 {
    // 数据集
    活跃NPC列表: 活跃NPC结构[];
    
    // 事件流
    进行中事件: 世界事件结构[];
    已结算事件: 世界事件结构[]; // 展示结果队列
    江湖史册: 世界事件结构[];   // 永久归档
}
