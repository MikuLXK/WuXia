const 构建判定提示词 = (配置: 判定难度配置): string => `
<判定难度协议>
【判定系统：${配置.名称}】
此模式定位：${配置.定位}

本协议用于“判定值与难度值”的计算与映射。
输出、命令与结构约束必须同时满足：<数据同步协议>、<输出结构与指令场景>、<COT预思考协议>、<战斗口径协议>。

## 1. 数据绑定（仅真实字段）
- 角色六维：\`力量/敏捷/体质/根骨/悟性/福源\`
- 生存状态：\`当前精力/最大精力\`、\`当前饱腹/最大饱腹\`、\`当前口渴/最大口渴\`
- 部位状态：七部位 \`当前血量/最大血量/状态\`
- 装备与功法：\`角色.装备\`、\`角色.功法列表\`
- 目标参考（若存在）：\`社交[i].攻击力/防御力/境界\` 或 \`战斗.敌方\`。
- 可选成长字段：\`角色.境界层级\`（缺失按 1）。

## 2. 判定日志格式（硬约束）
- 需要判定时，\`logs\` 必须输出 \`sender="【判定】"\`（或强制场景下 \`【NSFW判定】\`）。
- 固定格式：
\`行动名称｜结果｜触发对象 玩家:角色名 或 NPC:角色名｜判定值 X/难度 Y｜基础 B (说明)｜环境 E (说明)｜状态 S (说明)｜幸运 L｜装备 Q (说明,可选)\`
- 结果仅允许：\`成功/失败/大成功/大失败\`。

## 3. 核心计算公式
\`\`\`
最终判定值 = (基础能力 + 境界补正) + 幸运修正 + 环境修正 + 状态修正 + 装备修正 + 难度偏置
\`\`\`
- 难度偏置：${配置.难度偏置 >= 0 ? `+${配置.难度偏置}` : 配置.难度偏置}
- 境界补正：Lv1 +0 / Lv2 +120 / Lv3 +260 / Lv4 +420 / Lv5 +620 / Lv6+ 继续递增。
- 行动核心属性建议：
  - 近战攻伐：\`力量*10 + 敏捷*5\`
  - 闪避位移：\`敏捷*10 + 根骨*4\`
  - 护体硬抗：\`体质*10 + 根骨*6\`
  - 内功术法：\`悟性*10 + 根骨*5\`
  - 机变奇策：\`福源*10 + 悟性*4\`
- 战斗细分结算遵循 <战斗口径协议>（命中、闪避、伤害、部位系数）。

## 4. 难度值生成（独立于玩家模板）
- 优先使用“目标自身强度”：
  - \`对手基准值 = floor((目标攻击力 + 目标防御力)/2)\`（若字段存在）
- 若目标攻防不可读，则使用“目标境界层级映射基值”：
  - \`境界基准 = 180 + 目标境界层级*120\`
- 同境阈值增量：
  - 杂兵：\`${配置.同境阈值.杂兵 >= 0 ? `+ ${配置.同境阈值.杂兵}` : `- ${Math.abs(配置.同境阈值.杂兵)}`}\`
  - 精英：\`${配置.同境阈值.精英 >= 0 ? `+ ${配置.同境阈值.精英}` : `- ${Math.abs(配置.同境阈值.精英)}`}\`
  - 首领：\`+ ${配置.同境阈值.首领}\`
- 跨境增量：
  - 目标高 1 层：\`+${配置.跨境阈值.高一层}\`
  - 目标高 2 层：\`+${配置.跨境阈值.高两层}\`
- 非战斗 DC：
  - 简单 ${配置.非战斗DC.简单}
  - 中等 ${配置.非战斗DC.中等}
  - 困难 ${配置.非战斗DC.困难}
  - 极难 ${配置.非战斗DC.极难}

## 5. 修正值口径
- 环境修正：
  - 主场/熟地：${配置.环境修正.主场}
  - 恶劣天气/地形：${配置.环境修正.恶劣}
  - 阵形/队友支援：${配置.环境修正.支援}
  - 环境判断需读取 \`环境.天气.天气\` 与 \`环境.环境变量\`。
- 状态修正：
  - \`当前精力 <= 30%\`：${配置.状态修正.轻疲劳}
  - \`当前精力 <= 10%\`：${配置.状态修正.重疲劳}
  - 饱腹/口渴过低：${配置.状态修正.饱腹口渴}
  - 关键部位重伤与四肢失能：${配置.状态修正.部位重伤}
- 装备修正：
  - 明显克制：\`+10 ~ +30\`
  - 破损/不适配：\`-20 ~ -70\`

## 6. 结果映射表
| 差值（判定值-难度） | 结果 | 直接后果 |
|---|---|---|
| < ${配置.结果阈值.大失败} | 大失败 | 严重失误，触发重创/缴械/反制 |
| < 0 | 失败 | 行动未达成并承受代价 |
| >= 0 | 成功 | 达成目标并推进局面 |
| >= ${配置.结果阈值.大成功} | 大成功 | 额外收益（压制、破招、额外推进） |

## 7. 强制规则
- 流程固定：确定行动 -> 选核心属性 -> 生成难度值 -> 叠加修正 -> 输出判定行 -> 落地代价/收益。
- 失败不可无损跳过，必须在 \`tavern_commands\` 落地。
- ${配置.强制规则}

</判定难度协议>
`;

