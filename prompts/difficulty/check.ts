const 构建判定提示词 = (配置: 判定难度配置): string => `
【判定系统：${配置.名称}】
此模式定位：${配置.定位}

## 1. 数据绑定（仅使用项目已有路径）
- 角色六维：\`gameState.角色.力量/敏捷/体质/根骨/悟性/福源\`
- 生存状态：\`gameState.角色.当前精力/最大精力\`、\`当前饱腹/最大饱腹\`、\`当前口渴/最大口渴\`
- 部位状态：\`头部/胸部/腹部/左手/右手/左腿/右腿\` 的 \`当前血量/最大血量/状态\`
- 装备：\`gameState.角色.装备\`（主武器/副武器/防具等）
- 对手参考（存在时）：\`gameState.社交[i].攻击力/防御力/境界\`
- 可选成长字段：\`gameState.角色.境界层级\`（不存在默认按 1 级）

## 2. 判定输出要求（必须结构化）
- 出现判定时，必须在 \`logs\` 写入 \`sender: "【判定】"\`。
- 固定格式：
\`行动名称｜结果｜触发对象 玩家:角色名 或 NPC:角色名｜判定值 X/难度 Y｜基础 B (说明)｜环境 E (说明)｜状态 S (说明)｜幸运 L｜装备 Q (说明,可选)\`
- 结果仅允许：\`成功 / 失败 / 大成功 / 大失败\`。
- NSFW 场景默认不输出常规判定行；仅在强制失控/不可抵御结果时允许 \`【NSFW判定】\`。

## 3. 核心公式
\`\`\`
最终判定值 = (基础能力 + 境界补正) + 幸运修正 + 环境修正 + 状态修正 + 装备修正 + 难度偏置
\`\`\`
- 难度偏置：${配置.难度偏置 >= 0 ? `+${配置.难度偏置}` : 配置.难度偏置}
- 境界补正（按 \`境界层级\`）：Lv1 +0 / Lv2 +120 / Lv3 +260 / Lv4 +420 / Lv5 +620
- 行动核心属性：
  - 近战攻伐：\`力量*10 + 敏捷*5\`
  - 轻功闪避：\`敏捷*10 + 根骨*4\`
  - 护体硬抗：\`体质*10 + 根骨*6\`
  - 内功术法：\`悟性*10 + 根骨*5\`
  - 机变奇策：\`福源*10 + 悟性*4\`

## 4. 难度阈值（本档位完整定义）
- 对手基准值（可用时）：\`(攻击力 + 防御力) / 2\`
- 同境战斗：
  - 杂兵：\`对手基准 ${配置.同境阈值.杂兵 >= 0 ? `+ ${配置.同境阈值.杂兵}` : `- ${Math.abs(配置.同境阈值.杂兵)}`}\`
  - 精英：\`对手基准 ${配置.同境阈值.精英 >= 0 ? `+ ${配置.同境阈值.精英}` : `- ${Math.abs(配置.同境阈值.精英)}`}\`
  - 首领：\`对手基准 + ${配置.同境阈值.首领}\`
- 跨境挑战：
  - 对手高 1 层：同境阈值基础上 \`+${配置.跨境阈值.高一层}\`
  - 对手高 2 层：同境阈值基础上 \`+${配置.跨境阈值.高两层}\`
- 非战斗 DC：
  - 简单：${配置.非战斗DC.简单}
  - 中等：${配置.非战斗DC.中等}
  - 困难：${配置.非战斗DC.困难}
  - 极难：${配置.非战斗DC.极难}

## 5. 修正值口径
- 环境：
  - 主场/熟悉地形：${配置.环境修正.主场}
  - 黑暗、雨雪、狭窄、泥泞等恶劣条件：${配置.环境修正.恶劣}
  - 队友掩护、阵形支援：${配置.环境修正.支援}
- 状态：
  - \`当前精力 <= 30%\`：${配置.状态修正.轻疲劳}
  - \`当前精力 <= 10%\`：${配置.状态修正.重疲劳}
  - 饱腹或口渴过低：${配置.状态修正.饱腹口渴}
  - 关键部位重伤（头/胸/腹）与四肢失能：${配置.状态修正.部位重伤}
- 装备：
  - 明显克制：\`+10 ~ +30\`
  - 破损或不适配：\`-20 ~ -70\`

## 6. 结果判定表
| 差值（判定值-难度） | 结果 | 直接后果 |
|---|---|---|
| < ${配置.结果阈值.大失败} | 大失败 | 严重失误，触发重伤/缴械/反制 |
| < 0 | 失败 | 行动未达成并承受反噬或破绽代价 |
| >= 0 | 成功 | 达成目标，产生有效推进 |
| >= ${配置.结果阈值.大成功} | 大成功 | 关键收益（压制、破招、额外推进） |

## 7. 强制规则
- 流程必须为：确定行动 -> 选核心属性 -> 取阈值 -> 叠修正 -> 输出判定行。
- 失败不可无损跳过；需在 \`tavern_commands\` 落地对应代价（血量/精力/物资/剧情变量）。
- ${配置.强制规则}
`;

